export function getServiceBySlug(slug) {
  return servicePages.find(
    (service) => service.slug === slug || service.aliases?.includes(slug)
  );
}

export function getCanonicalServicePath(service) {
  return `/services/${service.slug}`;
}

export const serviceRouteSlugs = servicePages.flatMap((service) => [
  service.slug,
  ...(service.aliases || [])
]);
